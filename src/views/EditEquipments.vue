<template>
  <div class="md:flex">
    <!-- table -->
    <div class="md:w-1/2 p-4">
      <div class="table-container">
        <div class="overflow-x-auto">
          <table>
            <caption>
              Current Equipments
            </caption>
            <thead>
              <tr>
                <th scope="col">Name</th>
                <th scope="col">Description</th>
                <th scope="col">Image</th>
                <th scope="col">Edit</th>
              </tr>
            </thead>
            <tbody>
              <tr
                v-for="currentEquipment in displayEquipments"
                :key="currentEquipment.id"
              >
                <td data-label="Name">{{ currentEquipment.eqpName }}</td>
                <td data-label="Description">
                  {{ currentEquipment.description }}
                </td>
                <td data-label="ImageUrl">
                  <img
                    :src="currentEquipment.imageUrl"
                    :alt="currentEquipment.eqpName"
                  />
                </td>
                <td data-label="Edit">
                  <button
                    @click="confirmDelete = true"
                    class="md:mt-8 px-4 py-2 mx-2 bg-blue-500 text-white rounded hover:bg-blue-600"
                  >
                    Delete
                  </button>
                  <button
                    @click="showEditForm(currentEquipment.id)"
                    class="md:mt-8 px-4 py-2 bg-blue-500 text-white rounded hover:bg-blue-600"
                  >
                    Edit
                  </button>
                </td>
                <!-- Pop up -->
                <div
                  v-if="confirmDelete"
                  class="fixed inset-0 flex items-center justify-center bg-black bg-opacity-50"
                >
                  <div class="bg-white p-6 rounded shadow-lg">
                    <h2 class="text-xl font-semibold mb-4">
                      <button @click="confirmDelete = false" class="">
                        <i class="fa-solid fa-xmark"></i>
                      </button>
                    </h2>
                    <p class="mb-4">Are you sure you want to delete?</p>
                    <button
                      @click="deleteEqp(currentEquipment.id)"
                      class="bg-blue-500 hover:bg-blue-700 text-white font-bold py-2 px-4 rounded focus:outline-none focus:shadow-outline"
                    >
                      Yes, Delete
                    </button>
                  </div>
                </div>
              </tr>
            </tbody>
          </table>
          <div class="flex justify-center mt-5">
            <button
              @click="previousPage"
              v-if="currentPage > 1"
              class="bg-blue-500 hover:bg-blue-700 text-white font-bold py-2 px-4 mx-2 rounded focus:outline-none focus:shadow-outline"
            >
              Previous
            </button>
            <button
              @click="nextPage"
              v-if="currentPage < totalPages"
              class="bg-blue-500 hover:bg-blue-700 text-white font-bold py-2 px-4 rounded focus:outline-none focus:shadow-outline"
            >
              Next
            </button>
          </div>
        </div>
      </div>
    </div>
    <!-- Add, Edit Form -->
    <div class="md:w-1/2 p-4 my-auto">
      <div class="max-w-md mx-auto p-4">
        <form
          class="bg-white shadow-md rounded px-8 pt-6 pb-8 mb-4"
          @submit.prevent="addEquipment"
        >
          <h1 v-if="!isEditForm" class="text-center text-xl my-3 underline">
            Add New Equipment
          </h1>
          <h1 v-if="isEditForm" class="text-center text-xl my-3 underline">
            Edit Equipment
          </h1>
          <div class="mb-4">
            <label
              class="block text-gray-700 text-sm font-bold mb-2"
              for="equipmentName"
            >
              Equipment Name
            </label>
            <input
              class="shadow appearance-none border rounded w-full py-2 px-3 text-gray-700 leading-tight focus:outline-none focus:shadow-outline"
              id="equipmentName"
              type="text"
              v-model="eqpName"
              placeholder="Equipment Name"
            />
          </div>
          <div class="mb-4">
            <label
              class="block text-gray-700 text-sm font-bold mb-2"
              for="description"
            >
              Description
            </label>
            <textarea
              class="shadow appearance-none border rounded w-full py-2 px-3 text-gray-700 leading-tight focus:outline-none focus:shadow-outline"
              id="description"
              rows="4"
              v-model="description"
              placeholder="Description"
            ></textarea>
          </div>
          <div class="mb-4">
            <label
              class="block text-gray-700 text-sm font-bold mb-2"
              for="imageUrl"
            >
              Image URL
            </label>
            <input
              class="shadow appearance-none border rounded w-full py-2 px-3 text-gray-700 leading-tight focus:outline-none focus:shadow-outline"
              id="imageUrl"
              type="text"
              v-model="imageUrl"
              placeholder="Image URL"
            />
          </div>
          <div class="flex items-center justify-between">
            <button
              class="bg-blue-500 hover:bg-blue-700 text-white font-bold py-2 px-4 rounded focus:outline-none focus:shadow-outline"
              type="submit"
            >
              <p v-if="!isEditForm">Add Equipment</p>
              <p v-if="isEditForm">Edit Equipment</p>
            </button>
          </div>
        </form>
      </div>
    </div>
  </div>
</template>

<script>
import {
  addDoc,
  collection,
  deleteDoc,
  doc,
  getDoc,
  getDocs,
} from "firebase/firestore";
import db from "../firebase/init";
import { computed, onMounted, ref } from "vue";
export default {
  setup() {
    let currentEquipments = ref([]);
    let eqpName = ref("");
    let description = ref("");
    let imageUrl = ref("");
    let confirmDelete = ref(false);
    let isEditForm = ref(false);

    //getAllEquipments
    onMounted(async () => {
      const querySnapshot = await getDocs(collection(db, "equipments"));
      querySnapshot.forEach((doc) => {
        const EquipmentData = { id: doc.id, ...doc.data() };
        currentEquipments.value.push(EquipmentData);
      });
    });
    let addEquipment = async () => {
      const docRef = await addDoc(collection(db, "equipments"), {
        eqpName: eqpName.value,
        description: description.value,
        imageUrl: imageUrl.value,
      });
      // Fetch the newly added equipment from Firestore using the docRef
      const equipmentSnapshot = await getDoc(doc(db, "equipments", docRef.id));
      const newEquipment = { id: docRef.id, ...equipmentSnapshot.data() };

      // Update the local array currentEquipment with the new equipment
      currentEquipments.value.push(newEquipment);

      // Clear the input values after adding
      eqpName.value = "";
      description.value = "";
      imageUrl.value = "";
    };

    let deleteEqp = async (eqpId) => {
      try {
        const eqpRef = doc(db, "equipments", eqpId);
        await deleteDoc(eqpRef);
        currentEquipments.value = currentEquipments.value.filter(
          (currentEquipment) => currentEquipment.id !== eqpId
        );
        confirmDelete.value = false;
      } catch (error) {
        console.error("Error removing client:", error);
      }
    };

    //pagination
    const pageSize = 4;
    const currentPage = ref(1);

    const totalPages = computed(() =>
      Math.ceil(currentEquipments.value.length / pageSize)
    );

    const displayEquipments = computed(() => {
      const startIndex = (currentPage.value - 1) * pageSize;
      const endIndex = startIndex + pageSize;
      return currentEquipments.value.slice(startIndex, endIndex);
    });

    const nextPage = () => {
      if (currentPage.value < totalPages.value) {
        currentPage.value++;
      }
    };

    const previousPage = () => {
      if (currentPage.value > 1) {
        currentPage.value--;
      }
    };
    //pagination

    //Edit Equipments
    let showEditForm = (id) => {
      let editEquipment = currentEquipments.value.filter((equipment) => {
        return equipment.id === id;
      });

      eqpName.value = editEquipment[0].eqpName;
      description.value = editEquipment[0].description;
      imageUrl.value = editEquipment[0].imageUrl;

      //form edit button change
      isEditForm.value = true;
    };
    return {
      currentEquipments,
      eqpName,
      description,
      imageUrl,
      addEquipment,
      deleteEqp,
      displayEquipments,
      currentPage,
      nextPage,
      previousPage,
      totalPages,
      confirmDelete,
      showEditForm,
      isEditForm,
    };
  },
};
</script>

